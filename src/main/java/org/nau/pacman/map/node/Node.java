package org.nau.pacman.map.node;

public class Node {
    private int x;
    private int y;

    public Node() {

    }

    public Node(int x, int y) {
        setX(x);
        setY(y);
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
